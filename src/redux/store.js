import rootReducer from "./reducers/index";
import {
  persistStore,
  FLUSH,
  REHYDRATE,
  PAUSE,
  PERSIST,
  PURGE,
  REGISTER,
} from "redux-persist";
import { configureStore } from "@reduxjs/toolkit";

// export const store = createStore(rootReducer, middlewareEnhancer);

// export const store = configureStore({
